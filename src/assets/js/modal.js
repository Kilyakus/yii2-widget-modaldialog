		var wrapper,
			_destroy = function( exp ) {
				$( exp )
					.removeData( "resizable" )
					.removeData( "modal-resizable" )
					.off( ".resizable" )
					.find( ".modal-resizable-handle" )
						.remove();
			};

		// TODO: Unwrap at same DOM position
